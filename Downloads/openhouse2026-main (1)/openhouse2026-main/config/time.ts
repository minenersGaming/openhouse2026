const OPH_FIRST_TIME_DATE = "2026-01-09T07:00:00+07:00";
const OPH_SECOND_DAY_DATE = "2026-01-10T00:00:00+07:00";
const OPH_END_TIME_DATE = "2026-01-12T00:00:00+07:00";

export const OPH_FIRST_DAY_TIMESTAMP = new Date(OPH_FIRST_DAY_DATE).getTime();
export const OPH_FIRST_TIME_TIMESTAMP = new Date(OPH_FIRST_TIME_DATE).getTime();
export const OPH_SECOND_DAY_TIMESTAMP = new Date(OPH_SECOND_DAY_DATE).getTime();
export const OPH_END_TIME_TIMESTAMP = new Date(OPH_END_TIME_DATE).getTime();
export const IS_OPH_ONGOING = Date.now() >= OPH_FIRST_DAY_TIMESTAMP && Date.now() < OPH_END_TIME_TIMESTAMP;
